-- Atualizar vw_propostas para calcular dias_aberta baseado no campo data (data real de envio)
-- ao invÃ©s de created_at (data de entrada no sistema)

DROP VIEW IF EXISTS public.vw_insights_pool;
DROP VIEW IF EXISTS public.vw_propostas;

CREATE VIEW public.vw_propostas AS
SELECT 
  p.id,
  p.user_id,
  p.created_at,
  p.data AS periodo_dia,
  to_char(p.data::timestamp with time zone, 'YYYY-MM'::text) AS periodo_mes,
  EXTRACT(year FROM p.data)::integer AS periodo_ano,
  cl.nome AS cliente,
  cl.cidade,
  cl.bairro,
  l.origem AS canal,
  p.tipo_piso AS servico,
  p.m2,
  COALESCE(p.valor_total, 0::numeric) AS valor_total,
  COALESCE(p.liquido, 0::numeric) AS valor_liquido,
  CASE
    WHEN COALESCE(p.valor_total, 0::numeric) > 0::numeric 
    THEN COALESCE(p.liquido, 0::numeric) / p.valor_total * 100::numeric
    ELSE 0::numeric
  END AS margem_pct,
  p.status,
  p.forma_pagamento,
  p.desconto,
  -- Calcula dias aberta baseado na data real de envio da proposta (p.data)
  EXTRACT(day FROM now() - p.data::timestamp with time zone)::integer AS dias_aberta
FROM propostas p
JOIN clientes cl ON cl.id = p.cliente_id
LEFT JOIN leads l ON l.id = p.lead_id;

